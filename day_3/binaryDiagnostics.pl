/* AoC Day 3: Binary Diagnostics */

/* Part one*/

/* Some list operations */
nth(0, X, Y) :- X = [Y|_].
nth(N, X, Y) :- X = [_|T], M is N-1, nth(M, T, Y).

sumLst([X], [Y], [Z]) :- Z is X+Y.
sumLst([H1|T1], [H2|T2], [H3|T3]) :- H3 is H1+H2, sumLst(T1, T2, T3).

sumLstLst([X], X).
sumLstLst([H|T], Y) :- sumLstLst(T, Z), sumLst(H, Z, Y).

differences(_, [], []).
differences(N, [H|T], [H2|T2]) :- H2 is N-H, differences(N, T, T2).

/* Computing the gamma rate as a binary */
lessThan(_, [], []).
lessThan(N, [H|T], [1|T1]) :- H < N, lessThan(N, T, T1).
lessThan(N, [H|T], [0|T1]) :- H > N, lessThan(N, T, T1).
lessThan(N, [H|T], [0|T1]) :- H = N, lessThan(N, T, T1).

gammaBin(X, Y) :- length(X, N), sumLstLst(X, Sum), differences(N, Sum, Diff), H is N/2, lessThan(H, Diff, Y).

/* Computing the epsilon rate as a binary*/
inverted([], []).
inverted([1|T], [0|T1]) :- inverted(T, T1).
inverted([0|T], [1|T1]) :- inverted(T, T1).

epsilonBin(X, Y) :- gammaBin(X, G), inverted(G, Y).

/* Gamma/Delta rate */
decimalRev([], 0).
decimalRev([X|T], N) :- decimalRev(T, M), N is 2*M + X.
decimal(B, N) :- reverse(B, R), decimalRev(R, N).

gamma(X, Y) :- gammaBin(X, B), decimal(B, Y).
epsilon(X, Y) :- epsilonBin(X, B), decimal(B, Y).

/* Computing the power consumption */
power(P) :- diagnostics(X), gamma(X, G), epsilon(X, E), P is G*E.

